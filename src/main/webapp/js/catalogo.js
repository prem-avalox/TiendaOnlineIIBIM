
    function toggleCategoria() {
        const sidebar = document.getElementById("sidebarCategoria");
        sidebar.style.display = (sidebar.style.display === "none") ? "block" : "none";
    }

    function toggleBolsa() {
        const sidebar = document.getElementById("sidebarBolsa");
        sidebar.style.display = (sidebar.style.display === "none") ? "block" : "none";
    }